package com.example.demo.model;

public class ModifyRequest {
	private String username;
	private String group1;
	private String group2;
	private int itemsToBeSold;
	private int NoOfTotalItems;
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getGroup1() {
		return group1;
	}
	public void setGroup1(String group1) {
		this.group1 = group1;
	}
	public String getGroup2() {
		return group2;
	}
	public void setGroup2(String group2) {
		this.group2 = group2;
	}
	public int getItemsToBeSold() {
		return itemsToBeSold;
	}
	public void setItemsToBeSold(int itemsToBeSold) {
		this.itemsToBeSold = itemsToBeSold;
	}
	public int getNoOfTotalItems() {
		return NoOfTotalItems;
	}
	public void setNoOfTotalItems(int noOfTotalItems) {
		NoOfTotalItems = noOfTotalItems;
	}
	
	

}
